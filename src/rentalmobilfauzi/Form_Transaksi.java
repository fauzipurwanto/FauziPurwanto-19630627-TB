    private void tabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel3MouseClicked
        // TODO add your handling code here:
         try{
             databaru = false;
             
             int row = tabel3.getSelectedRow();
             String no = tabel3.getModel().getValueAt(row, 0).toString();
             
             Connection con = database.getConnection();
             String sql = "select * from transaksi where no = '"+no+"'";
             PreparedStatement pst = con.prepareStatement(sql);
             ResultSet rs = pst.executeQuery(sql);
             
             if (rs.next()) {
                 txtNoTransaksi.setText(rs.getString(1));
                 txtLamaSewa.setText(rs.getString(2));
                 dateMinjam.setDate(rs.getDate(3));
                 dateKembali.setDate(rs.getDate(4));
                 cm_nama.setSelectedItem(rs.getString(5));
                 txtHarga.setText(rs.getString(6));
                 merek.setSelectedItem(rs.getString(7));
                 txtTotal.setText(rs.getString(8));
             }
         } catch (SQLException ex) {
             Logger.getLogger(Form_Transaksi.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_tabel3MouseClicked

