   private void CariData (String key){
        try{
            Object[] judul_kolom = {"No Transaksi", "Lama Sewa", "Tanggal Minjam", "Tanggal Kembali","Nama Peminjam", "Merek", "Harga", "Total"};
            model = new DefaultTableModel(null,judul_kolom);
            tabel3.setModel(model);
            
            Connection con= database.getConnection();
            Statement stt=con.createStatement();
            model.getDataVector().removeAllElements();
            
            rsTransaksi=stt.executeQuery("SELECT * from transaksi WHERE no LIKE '%"+key+"%' OR nama_peminjam LIKE '%"+key+"%'");  
            while(rsTransaksi.next()){
                Object[] data={
                    rsTransaksi.getString("no"),
                    rsTransaksi.getString("lamasewa"),
                    rsTransaksi.getString("tanggal_minjam"),
                    rsTransaksi.getString("tanggal_pengembalian"),
                    rsTransaksi.getString("nama_peminjam"),
                    rsTransaksi.getString("harga"),         
                    rsTransaksi.getString("merek"),
                    rsTransaksi.getString("total")     
                };
               model.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
    }
